package operators;

public class Arithmetic {
	public static void main(String[]args) {
		int num1=23;
		int num2=3;

	System.out.println(num1+num2);
	System.out.println(num1-num2);
	System.out.println(num1*num2);
	System.out.println(num1/num2);
	System.out.println(num1%num2);

	int x=100;
	x++;
	System.out.println(x);

	int y=200;
	y--;
	System.out.println(y);
	}
}
