package operators;

public class Comparison {
	public static void main(String[]args) {
		int num1=200;
		int num2=100;
		System.out.println(num1<=num2);
		System.out.println(num1==num2);
		System.out.println(num1>=num2);
		System.out.println(num1!=num2);
		System.out.println(num1>num2);
		System.out.println(num1<num2);
	}

}
