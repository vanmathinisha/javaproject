package operators;

public class Comparisonop {
	public static void main(String[]args) {
		int no1=250;
		int no2=366;
		System.out.println(no1<=no2);
		System.out.println(no1>=no2);
		System.out.println(no1==no2);
		System.out.println(no1!=no2);
		System.out.println(no2>no1);
		System.out.println(no1<no2);
	}

}
