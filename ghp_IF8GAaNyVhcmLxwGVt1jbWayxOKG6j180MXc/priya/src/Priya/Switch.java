package Priya;

public class Switch {
	public static void main(String[]args) {
		int months=5;
		switch(months) {
		case 1:
			System.out.println("jan");
			break;
		case 2:
			System.out.println("feb");
			break;
		case 3:
			System.out.println("mar");
			break;
			default:
				System.out.println("no month");
		}
	}

}
